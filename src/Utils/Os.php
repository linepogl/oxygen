<?php

class Os {

	public static function EscapeUnixArg( $arg ) {
		return Str::Replace(
			array("'" ,'"' ," " ,"(" ,")" ,"`" ,"[" ,"]" ,"{" ,"}" ,"\\"  ,"&" ,"?" ,"*" ,"|" ,"$"  ,"<" ,">" ),
			array("\\'",'\\"',"\\ ","\\(","\\)","\\`","\\[","\\]","\\{","\\}","\\\\","\\&","\\?","\\*","\\|","\\\$","\\<","\\>"),
			$arg);
	}

}